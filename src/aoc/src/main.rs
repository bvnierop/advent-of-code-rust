use clap::{Parser, Subcommand};

#[derive(Parser)]
#[command(author, version, about, long_about = None)]
struct Cli {
    #[command(subcommand)]
    command: Commands,
}

#[derive(Subcommand)]
enum Commands {
    /// Prepare environment for solving a problem
    Prep,
}

fn main() {
    let cli = Cli::parse();

    match cli.command {
        Commands::Prep => {
            println!("Preparing environment...");
        }
    }
}

